package com.jxgm;

public class student {
    private String id;
    private String name;
    private Integer age;
    private String sex;

    public student() {
    }

    public student(String id, String name, Integer age, String sex) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.sex = sex;
    }
}
